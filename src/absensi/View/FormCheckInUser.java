    private void btnCheckInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckInActionPerformed
        try {
            Shift shift = (Shift) comboShift.getSelectedItem();

            if (shift == null) {
                JOptionPane.showMessageDialog(this, "Shift belum dipilih!");
                return;
            }

            int idKaryawan = userLogin.getIdKaryawan();
            int idShift = shift.getId_shift();
            String tanggal = LocalDate.now().toString();
            String jam = LocalTime.now().format(DateTimeFormatter.ofPattern("HH:mm:ss"));

            String status = jam.compareTo(shift.getJam_masuk()) <= 0
                    ? "Hadir Tepat Waktu"
                    : "Terlambat";

            Connection conn = Koneksi.getKoneksi();
            int idAbsensi = getNextIdAbsensi(conn);
            
            String cekSql = "SELECT * FROM tabel_absensi WHERE fk_karyawan=? AND tanggal=?";
            PreparedStatement cek = conn.prepareStatement(cekSql);
            cek.setInt(1, idKaryawan);
            cek.setString(2, tanggal);
            ResultSet rs = cek.executeQuery();

            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "Kamu sudah check-in hari ini!");
                return;
            }

            String sql = "INSERT INTO tabel_absensi (id_absensi, fk_karyawan, fk_shift, tanggal, waktu_checkin, status) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, idAbsensi);
            ps.setInt(2, idKaryawan);
            ps.setInt(3, idShift);
            ps.setString(4, tanggal);
            ps.setString(5, jam);
            ps.setString(6, status);

            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Check-in berhasil!\nStatus: " + status);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal check-in: " + e.getMessage());
        }
    }//GEN-LAST:event_btnCheckInActionPerformed

