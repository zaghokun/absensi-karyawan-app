    private void loadDataUser() {
        Karyawan k = controllerKaryawan.getById(userLogin.getIdKaryawan());

        if (k != null) {
            lblNamaValue.setText(k.getNama());
        }

        lblTanggalValue.setText(LocalDate.now().toString());
    }

    private void loadShift() {
        comboShift.removeAllItems();
        List<Shift> listShift = controllerShift.getAll();
        for (Shift s : listShift) {
            ((javax.swing.JComboBox) comboShift).addItem(s);
        }
    }

    private void mulaiJam() {
        timerJam = new javax.swing.Timer(1000, e -> {
            lblJamValue.setText(LocalTime.now().format(DateTimeFormatter.ofPattern("HH:mm:ss")));
        });
        timerJam.start();
    }
    
    private int getNextIdAbsensi(Connection conn) throws SQLException {
        String sql = "SELECT MAX(id_absensi) FROM tabel_absensi";
        PreparedStatement ps = conn.prepareStatement(sql);
        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            return rs.getInt(1) + 1;
        }

        return 1;
    }
